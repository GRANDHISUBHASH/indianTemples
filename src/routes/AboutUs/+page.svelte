    <h1>About Temples of India</h1>
    <p>Temples of India is a devotees led movement to create a comprehensive and detailed online repository of temples across the Bharatvarsha. Temples of India is a not-for-profit knowledge platform to document and store possibly all details of temples across Bharatvarsha.
        We aim to present each detail related to the temple such as its location, images of the temple, videos, open and close timings, etc contributed by devotees like you. You can search temples located nearby but unknown to you. Our goal is to acquaint users with the rich heritage of our country whose core lies in the temples.
        We are a group of students from IIT Bombay mentored by <a href="https://www.cse.iitb.ac.in/~ganesh/">Prof. Ganesh Ramakrishnan .</a></p>

